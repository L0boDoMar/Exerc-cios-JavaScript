/*Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à
soma dos quadrados dos três valores lidos.*/

var A = parseInt(prompt("Digite um valor"));
var B = parseInt(prompt("Digite um valor"));
var C = parseInt(prompt("Digite um valor"));

var somaQuad = (A**2)+(B**2)+(C**2);

alert("A soma dos quadrados dos valores lidos é: "+somaQuad);