/*Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do retângulo.*/ 

var base=parseInt(prompt("Informe a base do retângulo em cm:"));
var altura=parseInt(prompt("Informe a base do retângulo em cm:"));
var area= base * altura;
alert("A área do retângulo é de "+ area + " cm!");